# Considerando 2 números inteiros, faça a soma, subtração e
# multiplicação. Por fim, mostre os resultados.

print('Olá, para realizar os calculos solicitados, insira os números '
      'no pedido abaixo !')

n1= int(input('Digite o primeiro valor: '))
n2= int(input('Digite o segundo valor: '))

print(f'O valor da soma de {n1} + {n2} é = {n1+n2}')
print(f'O valor da subtração de {n1} - {n2} é = {n1-n2}')
print(f'O valor da multiplicação de {n1} x {n2} é = {n1*n2}')



